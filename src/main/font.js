// Build a 6-line big-text array from the input string + a centered subtitle.
// The result is colored cyan→blue→magenta like the NexTerm logo.
export function renderText(text, subtitle) {
  const COLORS = ['\x1b[1;96m', '\x1b[1;96m', '\x1b[1;94m', '\x1b[1;94m', '\x1b[1;95m', '\x1b[1;95m']
  const RESET = '\x1b[0m'

  const chars = (text || '').toUpperCase().split('').slice(0, 4) // up to 4 chars
  const glyphs = chars.map(c => ANSI_SHADOW[c] || ANSI_SHADOW[' '])
  if (glyphs.length === 0) glyphs.push(ANSI_SHADOW[' '])

  const rowVisible = (row) => '   ' + glyphs.map(g => g[row]).join(' ')
  const visibleWidth = rowVisible(0).length

  const out = []
  for (let row = 0; row < 6; row++) {
    out.push(`${COLORS[row]}${rowVisible(row)}${RESET}`)
  }

  if (subtitle) {
    const sub = `── ${subtitle.toUpperCase()} ──`
    const lead = Math.max(0, Math.floor((visibleWidth - sub.length) / 2))
    const trail = Math.max(0, visibleWidth - lead - sub.length)
    out.push(`\x1b[90m${' '.repeat(lead)}${sub}${' '.repeat(trail)}${RESET}`)
  }
  return out
}
